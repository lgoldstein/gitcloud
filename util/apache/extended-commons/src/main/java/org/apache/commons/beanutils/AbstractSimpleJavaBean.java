package org.apache.commons.beanutils;

import org.apache.commons.lang3.builder.EqualsBuilder;
import org.apache.commons.lang3.builder.HashCodeBuilder;
import org.apache.commons.lang3.builder.ToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;

/**
 * Provides some defaults for a simple JavaBean {@link #equals(Object)},
 * {@link #hashCode()} and {@link #toString()} using {@link EqualsBuilder},
 * {@link HashCodeBuilder} and {@link ToStringBuilder} respectively
 * @author Lyor G.
 * @since Jun 4, 2013 8:42:53 AM
 */
public abstract class AbstractSimpleJavaBean {
	protected AbstractSimpleJavaBean() {
		super();
	}

	@Override
	public int hashCode() {
		return HashCodeBuilder.reflectionHashCode(this, false);
	}

	@Override
	public boolean equals(Object obj) {
		if (obj == null)
			return false;
		if (this == obj)
			return true;
		if (getClass() != obj.getClass())
			return false;
		return isEquivalent(obj);
	}

	protected boolean isEquivalent(Object obj) {
		return EqualsBuilder.reflectionEquals(this, obj, false);
	}

	@Override
	public String toString() {
		return ToStringBuilder.reflectionToString(this, ToStringStyle.SHORT_PREFIX_STYLE, false);
	}
}
